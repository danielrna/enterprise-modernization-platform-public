function benchmarkFilterScript() {
  return `<script>
  const rows = Array.from(document.querySelectorAll('[data-benchmark-row]'));
  const search = document.querySelector('#benchmark-search');
  const source = document.querySelector('#source-filter');
  const validation = document.querySelector('#validation-filter');
  const count = document.querySelector('#benchmark-count');
  function applyBenchmarkFilters() {
    const query = search.value.trim().toLowerCase();
    const selectedSource = source.value;
    const selectedValidation = validation.value;
    let shown = 0;
    for (const row of rows) {
      const matchesQuery = !query || row.dataset.search.includes(query);
      const matchesSource = selectedSource === 'all' || row.dataset.source === selectedSource;
      const matchesValidation = selectedValidation === 'all' || row.dataset.validation === selectedValidation;
      const visible = matchesQuery && matchesSource && matchesValidation;
      row.hidden = !visible;
      if (visible) shown += 1;
    }
    count.textContent = shown + ' shown';
  }
  for (const control of [search, source, validation]) control.addEventListener('input', applyBenchmarkFilters);
</script>`;
}

