function formatReadiness(readiness) {
  return readiness === null ? 'N/A' : `${readiness}%`;
}

function renderReadinessBadge(readiness) {
  if (readiness === null || readiness === undefined) return '<span class="badge neutral">N/A</span>';
  const value = Number(readiness);
  const tone = value >= 80 ? 'good' : value >= 65 ? 'warn' : 'bad';
  return `<span class="badge ${tone}">${escapeHtml(value)}%</span>`;
}

function renderStatusBadge(status, kind = 'validation') {
  const value = String(status || 'not_requested');
  const tone = statusTone(value, kind);
  return `<span class="badge ${tone}">${escapeHtml(formatStatus(value))}</span>`;
}

function statusTone(status, kind) {
  if (kind === 'source') return status === 'checkout' ? 'good' : 'neutral';
  if (status === 'passed') return 'good';
  if (status === 'failed') return 'bad';
  if (status === 'skipped' || status === 'not_requested') return 'neutral';
  return 'warn';
}

function formatStatus(status) {
  return status.replaceAll('_', ' ');
}

function summarizeBenchmarkIndex(reports) {
  return {
    checkout: reports.filter((report) => (report.source || 'catalog') === 'checkout').length,
    passed: reports.filter((report) => report.validation?.status === 'passed').length,
    ready: reports.filter((report) => Number(report.readiness) >= 80).length
  };
}

