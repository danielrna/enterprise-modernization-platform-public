function shortRepository(repository) {
  return repository.replace('https://github.com/', '');
}

async function loadBenchmarkReports(benchmarksDir) {
  const entries = await fs.readdir(benchmarksDir, { withFileTypes: true }).catch(() => []);
  const reports = [];
  for (const entry of entries) {
    if (!entry.isDirectory()) continue;
    const slug = entry.name;
    const reportPath = path.join(benchmarksDir, slug, 'report.json');
    const report = await readJson(reportPath);
    if (!report) continue;
    reports.push(normalizeReport(slug, report));
  }
  return reports.sort(compareBenchmarks);
}

async function readJson(file) {
  try {
    return JSON.parse(await fs.readFile(file, 'utf8'));
  } catch {
    return null;
  }
}

function normalizeBenchmarks(benchmarks) {
  return benchmarks.map((benchmark) => ({
    slug: benchmark.slug,
    name: benchmark.name,
    repository: benchmark.repository,
    pack: benchmark.pack,
    springBootVersion: benchmark.springBootVersion,
    readiness: benchmark.readiness ?? null,
    source: benchmark.source || 'catalog',
    validation: benchmark.validation || { status: 'not_requested', confidence: 0 },
    findings: {
      total: benchmark.findings?.length ?? 0,
      critical: benchmark.findings?.filter((finding) => finding.severity === 'critical').length ?? 0,
      warning: benchmark.findings?.filter((finding) => finding.severity === 'warning').length ?? 0,
      info: benchmark.findings?.filter((finding) => finding.severity === 'info').length ?? 0
    }
  })).sort(compareBenchmarks);
}

function normalizeReport(slug, report) {
  return {
    slug,
    name: report.project?.name || slug,
    repository: report.project?.source?.startsWith('https://github.com/')
      ? report.project.source
      : `https://github.com/${slug}`,
    pack: report.pack || 'unknown',
    springBootVersion: report.project?.springBootVersion || report.dependencies?.springBootVersion || 'unknown',
    readiness: report.readiness?.overall ?? null,
    source: report.benchmark?.source || 'catalog',
    validation: report.benchmark?.validation || { status: 'not_requested', confidence: 0 },
    findings: {
      total: report.findings?.length ?? 0,
      critical: report.readiness?.counts?.critical ?? 0,
      warning: report.readiness?.counts?.warning ?? 0,
      info: report.readiness?.counts?.info ?? 0
    }
  };
}

function compareBenchmarks(left, right) {
  if (left.pack !== right.pack) return left.pack.localeCompare(right.pack);
  return left.name.localeCompare(right.name);
}

function escapeHtml(value) {
  return String(value ?? '')
    .replaceAll('&', '&amp;')
    .replaceAll('<', '&lt;')
    .replaceAll('>', '&gt;')
    .replaceAll('"', '&quot;')
    .replaceAll("'", '&#039;');
}

function normalizeHtml(html) {
  return html.replace(/[ \t]+$/gm, '');
}
